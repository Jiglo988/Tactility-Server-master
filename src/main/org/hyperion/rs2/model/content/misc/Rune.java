package org.hyperion.rs2.model.content.misc;

public class Rune {

	private int runeId, level;
	private double xp;
	private int[] levels;

	public int getRuneId() {
		return runeId;
	}

	public int getLevel() {
		return level;
	}

	public double getExp() {
		return xp;
	}

	public int[] getLevels() {
		return levels;
	}
}