package org.hyperion.rs2.model.content.misc;

public class Herb {
	private int herbId, cleanId, level;
	private double experience;

	public int getHerbId() {
		return herbId;
	}

	public int getCleanHerb() {
		return cleanId;
	}

	public int getLevel() {
		return level;
	}

	public int getExperience() {
		return (int) experience;
	}

}