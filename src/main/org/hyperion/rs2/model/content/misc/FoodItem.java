package org.hyperion.rs2.model.content.misc;

public class FoodItem {
	private int id, heal, newId, skillId, skillAdd;
	private boolean drink;

	public int getId() {
		return id;
	}

	public int getHeal() {
		return heal;
	}

	public int getNewId() {
		return newId;
	}

	public int getSkillId() {
		return skillId;
	}

	public int getSkillAdd() {
		return skillAdd;
	}

	public boolean isDrink() {
		return drink;
	}
}